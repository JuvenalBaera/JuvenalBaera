fun main(){
    var n1= 10
    var n2 = 3

    var sum: Int = n1 + n2
    var sub: Int = n1 - n2
    var mult: Int = n1 * n2
    var div: Float = n1.toFloat() / n2
    var rem: Int = n1 % n2

    println("Sum...........: $sum")
    println("Subtraction...: $sub")
    println("Multiplication: $mult")
    println("Division......: $div")
    println("Remainder.....: $rem")
}