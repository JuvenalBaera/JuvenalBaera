fun main(){
    val n1 = 10
    val n2 = 5

    println("Greater than .............(>): ${n1 > n2}")
    println("Greater than or equal to (>=): ${n1 >= n2}")
    println("Less than ................(<): ${n1 < n2}")
    println("Less than or equal to ...(<=): ${n1 <= n2}")
    println("Equal to ................(==): ${n1 == n2}")
}