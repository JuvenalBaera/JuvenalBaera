fun main(){
    var myVar: Int  // Declaration
    myVar = 21      // Assignment

    // Declaration and assignment
    var mySecondVar: String  = "This is text"
    // Numbers
    var age: Byte = 21
    var quantity: Int = 1000
    var weight: Float = 58.5f
    var pi: Double = 3.1415659214

    // Text
    var letter: Char = '@'
    var name: String = "Juvenal"

    // Boolean
    var alive: Boolean = true
    var maried: Boolean = false

    // print the value inside variable
    println("myVar has......: $myVar")
    println("mySecondVar has: $mySecondVar")
    println("Age has........: $age")
    println("Quantity has...: $quantity")
    println("Weight has.....: $weight")
    println("Pi has.........: $pi")
    println("Letter has.....: $letter")
    println("Name has.......: $name")
    println("Alive has......: $alive")
    println("Maried has.....: $maried")
}
